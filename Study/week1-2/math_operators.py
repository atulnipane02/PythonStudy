x = 10
y = 20
z = 30

# Addition
print(x+y+z)

# Substraction
print(z-x-y)

# Multiplication
print(x*y*z)

# Division - Normal division results into float
print(z/y/x)

# Floor Division rresults into integer
print(y // z)

# Exonentiation - Power to the number
print(x**y)

# Modulo operator - Remainder
print(y % z)

