while True:
    print("1. For Number 1")
    print("2. For Number 2")
    print("3. Exit from the loop")
    option = input("Please select your option")
    if option == "1":
        print("Number 1 is selected")
    elif option == "2":
        print("Number 2 is selected")
    elif option == "3":
        print("Exiting from the Loop")
        break
    else:
        print("Invalid option selected")
