"""Primitive Data Types"""

name = "Atul Nipane"
age = 32
cash = 100.25
retired = False

print(name)
print(type(name))
print(age)
print(type(age))
print(cash)
print(type(cash))
print(retired)
print(type(retired))

"""Composite Data Types"""
nucamp_locations = ["Seattle", "Tacoma", "Bellevue"]
Bob_Info = {"name": "Bob", "age": 32, "cash": 100.25, "retired": False}
my_tuple = ("apple", "banana", "cherry")
my_set = {"cats", "dogs", "birds"}

print(nucamp_locations)
print(type(nucamp_locations))
print(Bob_Info)
print(type(Bob_Info))
print(my_tuple)
print(type(my_tuple))
print(my_set)
print(type(my_set))
